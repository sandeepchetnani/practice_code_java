class NarrowingDouble
{
public static void main(String args[])
{

double d=50.2234444;
byte b=(byte)d;
short s=(short)d;
char c=(char)d;
int i=(int)d;
long l=(long)d;
float f=(float)d;

System.out.println(b);
System.out.println(s);
System.out.println(c);
System.out.println(i);
System.out.println(l);
System.out.println(f);



}
}